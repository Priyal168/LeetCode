class Solution
{
  public:
    int findOnce(int arr[], int n)
    {
        unordered_map<int,int> umap;
        for(int i=0;i<n;i++){
            umap[arr[i]]++;
        }
        for(auto i:umap){
            if(i.second==1){
                return i.first;
            }
        }
        return -1;
    }
};

